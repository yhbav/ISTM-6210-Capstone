    <p>Starting after TI10, the Dota Pro Circuit will introduce a new system that presents competitive Dota in a more scheduled and consistent way during the year and features a better structure for the development of Tier 2 and Tier 3 teams.</p>
    <p>The year will be divided in 3 seasons. Each season will be composed of 6 regional leagues, leading into a Major with the best teams in the world. The Dota year will conclude, as usual, with The International.</p>
    <h2 style="margin-bottom: 20px; margin-top: 50px;"><u><b>Regional Leagues:</b></u></h2>
    <p>Each of the six leagues will feature a prize pool of USD 280,000 per season. Leagues will feature two divisions with 8 teams in the Upper Division and 8 in the Lower division, for a total of 96 teams participating across the world. After each season, the 2 bottom teams of the Upper Division will swap places with the top 2 of the Lower Division. The bottom 2 teams of the Lower Division will be eliminated from the league and replaced with 2 new teams coming from Open Qualifiers to allow for new teams and talent to be introduced into the scene over time.</p>
    <p>We believe the consistency and regularity of the league throughout the year, paired with a flat and deep prize pool distribution will, over time, nurture healthy and strong Tier 2/3 competition.</p>
    <p>The leagues will have a duration of six weeks, and each region will consist of a full Bo3 Round Robin among all teams. In order to make it easier to follow your favorite region and teams, all matches will be in consistent date and time slots throughout the year for each region.</p>
    <p>All Upper Division matches will be presented from studio broadcasts. All Lower Division matches will be featured on DotaTV.</p>
    <p>Each region will have 3 competition days a week, schedule will be as follows, All times in PST. Each slot represents a Best of 3 series:</p>
