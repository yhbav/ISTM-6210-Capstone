    <p>For a team to be eligible to participate in a region, 3 or more of the players need to reside in the area they are competing in. Additionally, teams will be able to use a stand-in for up to 4 of their matches as long as that stand-in is either competing in a lower division or not competing in a league at all.</p>
    <p>If a team decides to change regions, they will have to enter the region through open qualifiers and climb through that region’s Lower Division.</p>
    <p>For the duration of each season, from the beginning of the league until the end of the Major, all rosters will be locked. After the Major concludes and until the beginning of the next season, roster changes will be possible. Each player change will incur a 15% penalty on current points for that team.</p>
